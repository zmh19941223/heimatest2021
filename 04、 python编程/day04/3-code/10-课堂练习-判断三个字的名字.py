list1 = ['张三', '李四', '刘老二', '王麻子', '王达成', '隔壁老王']
# 写代码判断列表中名字为三个字的人有几个

# 思路,首先得把每个名字遍历一遍
num1 = 0   # 存放名字为3个字的出现次数
for n in list1:  # n是列表中的每个成员,list1中有几个成员循环几次
    sum = 0
    for a in n:  # a是字符串n中的每个字符,n中有几个字符for循环几次
        sum += 1
    if sum == 3:
        num1 += 1
print(num1)
# 只要知道sum出现3有几次,就是这个答案

