tuple1 = ('刘备', '关羽', '张飞') # 定义了一个元组,有三个成员
tuple2 = ()  # 定义了一个空元组
tuple3 = ('刘备', )  # 如果元组中只有一个值,那么后面必须写逗号
tuple4 = '刘备', '关羽', '张飞'  # 定义了一个元组,有三个成员
tuple5 = '刘备',  # 如果元组中只有一个值,那么后面必须写逗号
tuple6 = "刘备"  # 定义的不是元组,是字符串
tuple7 = ("刘备")  # 定义的不是元组,是字符串
print(tuple1)
print(tuple2)
print(tuple3)
print(tuple4)
print(tuple5)
print(tuple6)
print(tuple7)